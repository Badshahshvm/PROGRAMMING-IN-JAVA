public class table
{
    public static void main(String[] args)
    {
        int i;
        char x;
        for(i=1;i<=10;i++)
        {
            System.out.println(2*i);
        }
        for(i='a';i<='z';i++)
        {
            System.out.println(i);
        }
        for(x='a';x<='z';x++)
        {
            System.out.println(x);
        }
    }
}
